    barn_name TEXT,
    nkr_reg_number TEXT,
    tattoo TEXT,
    ear_tag TEXT,
    rfid_tag TEXT,
    dob TEXT,
    sex TEXT NOT NULL,
    color TEXT,
    markings TEXT,
    registry TEXT,
    breed_type TEXT,
    herd_book TEXT,
    vgl_id TEXT,
    eid_type TEXT,
    eid_placement TEXT,
    id_tag_number TEXT,
    id_tag_placement TEXT,
    scrapie_tag TEXT,
    eye_color TEXT,
    ear_type TEXT,
    horn_type TEXT,
    description TEXT,
    ownership_status TEXT,
    breed TEXT DEFAULT 'Kiko',
    dam_id BIGINT REFERENCES public.animals(id) ON DELETE SET NULL,
    sire_id BIGINT REFERENCES public.animals(id) ON DELETE SET NULL,
    dam_name TEXT,
    sire_name TEXT,
    dam_reg_number TEXT,
    sire_reg_number TEXT,
    status TEXT NOT NULL DEFAULT 'active',
    birth_weight_lbs DOUBLE PRECISION,
    purchase_date TEXT,
    purchase_price DOUBLE PRECISION,
    sold_date TEXT,
    sold_price DOUBLE PRECISION,
    sold_to TEXT,
    deceased_date TEXT,
    deceased_reason TEXT,
    photo_path TEXT,
    notes TEXT,
    is_herd_sire INTEGER NOT NULL DEFAULT 0,
    is_registered INTEGER NOT NULL DEFAULT 0,
    second_registry TEXT,
    second_reg_number TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
