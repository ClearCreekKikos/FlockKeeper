-- Create a storage bucket called "flockkeeper"
INSERT INTO storage.buckets (id, name, public)
VALUES ('flockkeeper', 'flockkeeper', false)
ON CONFLICT (id) DO NOTHING;

-- Enable RLS on storage.objects
ALTER TABLE storage.objects ENABLE ROW LEVEL SECURITY;

-- Grant access policy for the storage bucket
-- Enforces that users can only upload/access objects under their own UUID prefix folder: e.g. "auth.uid()/photos/..."
CREATE POLICY "Users can manage their own files in flockkeeper bucket"
ON storage.objects
FOR ALL
TO authenticated
USING (
    bucket_id = 'flockkeeper' 
    AND (storage.foldername(name))[1] = auth.uid()::text
)
WITH CHECK (
    bucket_id = 'flockkeeper' 
    AND (storage.foldername(name))[1] = auth.uid()::text
);
